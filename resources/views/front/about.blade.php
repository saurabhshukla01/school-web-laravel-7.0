            <h3>
               What is an About Us page?
            </h3>
               <p style="font-size:18px;">
The About Us page of your site is the page where you get to talk about yourself and present how awesome of a person you are. It’s the place that gives you the opportunity to talk about what you do and why it matters.

Every successful entrepreneur knows that a well-designed About Us page brings ‘all the boys and gals in the yard’ which means that it acts as the first point of contact between the ‘creator’ and the ‘receiver’ or else in marketing terms, the ‘seller’ and the ‘buyer’.

Why is it important?
A recent survey from KoMarketing, revealed that the first thing 52 percent of participants want to see on a website’s home page is the About Us/Company Information section. On a comparison table the ‘About Us’ page comes at third place following the Product/Services (top priority) and Contact Information (second place).

What’s great about this discovery is that all three company elements can be combined into one page constituting all the content you should care most about and need to present. By making the best use of the space (on the site) and the time visitors stay on your page, it provides all the information they need to decide if they want to engage with you or not.
</p>
<h3>What does it do?</h3>
<p style="font-size:18px;">
The purpose of an About Us page is to draw people’s attention and help them understand who you are and what you have to offer. With the ‘About Us’ page, as an online instructor you offer to students the opportunity to meet you by presenting some essential information about your personal and professional profile.

Apart from that, an About Us page also gives you the chance to establish a relationship with people you haven’t met yet, providing you with an extra competitive advantage over other course sellers who are promoting the same product, but fail to become well-known to the public.

In simple terms, here’s what an About Us page can do for you:

Introduce your brand: this gives you the opportunity to put a face on your ‘business’, to communicate the story behind it and why you started it in the first place. It’s where you tell people ‘We are X team and we do this and this’ or if you are solopreneur ‘I am a [Your Job Title/Role] and I can help you with this’.
Establish a relationship: it helps visitors understand your brand and learn more about how it works and build a solid ‘common ground’.
Present persuasive content: resources that encourage visitors to stay on your site and eventually convince them to buy from you.
Strengthen SEO: offers the potential to increase site traffic from customers looking up for you on Google searches. This requires that you use the right keywords and that you effectively customise the text and the visuals you use e.g. photos, videos etc. Also, you have to think about what you want your site visitors to do (e.g. what page to visit next) and create appropriate CTAs for that.
Answer basic enquiries: explain how you or your business can help site visitors solve their problems.
Offer authenticity: site visitors or people hoping to invest in you in any way want to make sure that your business is legit.
Check availability: gives the opportunity to site visitors to check if your business/school serves their niche – interests, and can serve them geographically.
Attract future employees: provides the chance to site visitors to find more information on career opportunities with your online business if you offer any.
What’s crucial to know about the About Us page is that it becomes necessary for ecommerce sites. Selling a course isn’t the same as selling a physical product – and not to mention, is far less-common and unique. Hence, the About Us page can offer more detailed information and an in-depth explanation into what you are promoting and selling online.
